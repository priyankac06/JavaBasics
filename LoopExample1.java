package Programs;

public class LoopExample1 {

	public static void main(String[] args) {
		// Print Name 5times
		for(int i=1;i<=5;i++)
		{
			System.out.println("Priyanka Abhimanyu Bhoir");
		}

		// Print 50 to 90
		for(int i=50;i<=90;i++)
		{
			System.out.println(i);
		}
		// Print -50 to -90
		for(int i=-50;i>=-90;i--)
		{
			System.out.println(i);
		}
		
	}

}
