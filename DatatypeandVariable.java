package Programs;

public class DatatypeandVariable {

	public static void main(String[] args) {
		// Syntax: datatype varibale = value;
		//Number datatype
		byte a = 100; //declaration and initialization of a variable
		short s = 300;//declaration and initialization of a variable
		int age = 30;//declaration and initialization of a variable
		int p1; //declaration
		p1=150; //initialization
		
		long salary = 3202000244l;//declaration and initialization of a variable
		
		//decimal datatype
		double pi = 3.148;//declaration and initialization of a variable 
		pi=2.12;
		System.out.println(pi);
		// we can give 17 or 18 decimal values in double datatype
		float q = 3.15f;//declaration and initialization of a variable
		
		//character datatype
		char city = 'M';//declaration and initialization of a variable
		
		//Boolean datatype
		boolean condition = true;//declaration and initialization of a variable
		
		//String datatype
		String name = "PRIYANKA@123";//declaration and initialization of a variable
		
		
	}

}
