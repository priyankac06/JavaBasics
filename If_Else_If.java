package Programs;

public class If_Else_If {

	public static void main(String[] args) {
		// If else if block
//If any first condition "TRUE",it will print, 
//it won't check for other condition
		int a = 20;
		int b = 20;
		int c = 30;
		if(a>b)
		{
			System.out.println("1");
		}else if(a==b)
		{
			System.out.println("2");
		}else if(b<c)
		{
			System.out.println("3");
		}else if(b==c)
		{
			System.out.println("4");
		}

	}

}
