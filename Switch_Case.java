package Programs;

public class Switch_Case {

	public static void main(String[] args) {
		// Switch Case
		switch(7)
		{
		case 1:System.out.println("Chrome Browser Launch");
		break;
		case 2:System.out.println("Safari Browser Launch");
		break;
		case 3:System.out.println("Edge Browser Launch");
		break;
		case 4:System.out.println("Firefox Browser Launch");
		break;
		default:System.out.println("Browser not supported");
		}

	}

}
